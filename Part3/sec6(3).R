### Vector = 1차원 (동일한 형태의 데이터를 모아서 함께 저장하는 것)
### Matrix = 2차원
### Array  = 3차원

###==================================================================
### Array 다루기 [array(x,dim=c())] =================================
###==================================================================

array1 <- array(c(1:12), dim=c(4,3))    # (4*3)=12 데이터 갯수를 맞춰야한다. 
array2 <- array(c(1:12), dim=c(2,2,3))  # dim=(c(행,열,갯수))
array2[,,1] ; array2[,,2] ; array2[,,3] # [행,열,차원(갯수)] 

