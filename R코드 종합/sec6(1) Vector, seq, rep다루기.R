###===================================================================================
### seq rep 다루기  ==================================================================
###===================================================================================

var6  <- seq(1:6)         ; var6
var7  <- seq(2,-2)        ; var7   # + 에서 - 순서로.
var8  <- seq(2,10,2)      ; var8   # (짝수) 2씩 증가시키면서 값 할당.
var9  <- seq(1,10,2)      ; var9   # (홀수)

var10 <- rep(1:3, 2)      ; var10  # 지정값 반복횟수.
var11 <- rep(1:3, each=3) ; var11  #같은값으로 each만큼 반복.


###===================================================================================
### Vector에 특정한 문자 포함여부 찾기 (원하는 숫자 %in% ) ===========================
###===================================================================================

3 %in% var6  # TRUE
9 %in% var6  # FALSE


