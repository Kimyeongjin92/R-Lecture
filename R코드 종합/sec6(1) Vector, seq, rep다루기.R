### Vector = 1차원 (동일한 형태의 데이터를 모아서 함께 저장하는 것)
### Matrix = 2차원
### Array  = 3차원


###===================================================================================
### Vector 다루기. (입력, 전환, 생략, 색인) ==========================================
### CRUD (Create Retrieve Update Delete의 과정을 할 수 있어야한다.)===================
###===================================================================================

c(1,2,3,4,5)
c(1,2,3,4,'5')
vec1<-c(12,23,34,45,56); vec1

vec1[3]        # 3번째 
vec1[-3]       # 3번째를 제외한 모든값.
vec1[3]  <- 6  # 변수값을 바꿀 때
vec1[10] <-10  # 6~9까지의 값은 NA(결측값)으로 생성된다.
vec1[1:3]
vec1[-1:-3]
vec1[-(1:3)]
vec1 <- c(vec1, 7, 6, 9, 8, 9) ;vec1

###===================================================================================
### append함수 append(x, 원하는숫자, after=위치) =====================================
###===================================================================================

append(vec1,10,after=5)       # after 5는 index의 5(즉, 다섯번째 뒤에 10을 입력)
append(vec1,c(10,11),after=0) # after 0은 제일 앞 위치함.
vec1 <- append(vec1,c(10,11),after=5) ; vec1

length(vec1)  #변수의 길이 ex)5
vec1[length(vec1)]
vec1[1:(length(vec1)-2)] 


###===================================================================================
### Vector로 연산하기1 (union 데이터타입이 다른 2개의 vector를 합친다) ================
###===================================================================================

c(1,2,3) + 1        #각각에 대해 1씩 더하기
c(1,2,3) + c(4,5,6)

var1 <- c(1,2,3,4,5) # var1<- seq(1:5)
var2 <- c(4,5,6,7,8) # var2<- seq(4:8)
var3 <- c('3','4',5) ; var3
var4 <- c(1,2,3)
var5 <- c(1,2,3,4,5,6)

var1 + var2      # 5,7,9
var1 + var3      # 데이터의 형이 다를 경우 union을 사용한다.
union(var1,var3) # "1" "2" "3" "4" "5"
var1 + var4      # 작은 갯수가 반복해서 더해준다. But 갯수 불일치로 경고메세지가 뜸. 
var4 + var5      # 3개 + 6개로 2배수에 맞기 때문에 경고메세지 없음.

var1 - var2
var1 - var4

###===================================================================================
### Vector로 연산하기2 [setdiff(a,b) intersect(a,b)] =================================
###===================================================================================

setdiff(var1,var2)   # var1-var2 차집합 
setdiff(var2,var1)   # var2-var1 
intersect(var1,var2) # 교집합


###===================================================================================
